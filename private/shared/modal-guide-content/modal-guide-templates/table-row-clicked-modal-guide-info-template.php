<h1>
    Client & Staff Records
</h1>
<h2>
    Things To Know:
</h2>
<ul>
    <li>
       <span class="bold">EDIT CHECKBOX:</span> This is used to prevent any potential mistakes when viewing any existing records. Just click the edit checkbox in the top right of the modal to enable editing if you wish to change anything.
    </li>
    <li>
       <span class="bold">SAVE BUTTON:</span> The save button appears when the edit checkbox is checked. 
    </li>
    <li>
        <span class="bold">DELETE BUTTON:</span> Click the delete button to delete the information.
    </li>
</ul>
<h2>
    Schedule Section:
</h2>
<ul>
    <li>
        Any clients or staff with assigned schedules will show up in the schedules section of the modal.
    </li>
    <li>
       <span class="bold">VIEWING SCHEDULES:</span> Just click the schedule if you wish to view it. 
    </li>
    <li>
        <span class="bold">ADDING A SCHEDULE:</span> Click the <span class="new-schedule-symbol"><i class="fa fa-plus"></i></span> to the right of the schedule label.
    </li>
</ul>